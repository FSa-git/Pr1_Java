package Neue_Erklärung_für_Luca;

public class Integer { // Die Integer Klasse braucht man nur wegen der Aufgabenstellung

	protected int wert; // (Von überall zugreifbar = public, mit protected macht man noch getter)

	public Integer(int wert) {
		this.wert = wert;
	}

	public int getWert() {
		return wert;
	}
	
	
	
	
}
